const fs = require("node:fs");
const path = require("node:path");
const { execFileSync } = require("node:child_process");

const repoRoot = path.resolve(__dirname, "..");
const platformKey = process.platform === "win32" ? "win32" : process.platform === "darwin" ? "darwin" : process.platform;
const archKey = process.arch === "x64" ? "x64" : process.arch === "ia32" ? "ia32" : process.arch === "arm64" ? "arm64" : process.arch;
const isWindows = process.platform === "win32";
const targetRoot = path.join(repoRoot, "tool-runtime", `${platformKey}-${archKey}`, "deface");
const platformRoot = path.dirname(targetRoot);
const venvRoot = path.join(targetRoot, "venv");
const venvBin = path.join(venvRoot, isWindows ? "Scripts" : "bin");
const venvPython = path.join(venvBin, isWindows ? "python.exe" : "python");
const venvDeface = path.join(venvBin, isWindows ? "deface.exe" : "deface");
const licensePath = path.join(targetRoot, "LICENSES.md");
const manifestPath = path.join(targetRoot, "wanjuan-bundled-deface.json");
const defaceVersion = process.env.WANJUAN_BUNDLED_DEFACE_VERSION || "1.5.0";
const bootstrapRoot = path.join(repoRoot, ".wanjuan-build-tools", `${platformKey}-${archKey}`, "virtualenv-bootstrap");
const bootstrapPython = path.join(bootstrapRoot, isWindows ? "Scripts" : "bin", isWindows ? "python.exe" : "python");

function run(command, args, options = {}) {
  console.log([command, ...args].join(" "));
  execFileSync(command, args, {
    cwd: repoRoot,
    stdio: "inherit",
    env: process.env,
    ...options
  });
}

function tryRun(command, args, options = {}) {
  try {
    run(command, args, options);
    return true;
  } catch (error) {
    console.warn(`Command failed, will try fallback if available: ${[command, ...args].join(" ")}`);
    console.warn(error?.message || error);
    return false;
  }
}

function commandWorks(command, args) {
  try {
    execFileSync(command, args, {
      cwd: repoRoot,
      stdio: "ignore",
      timeout: 30000
    });
    return true;
  } catch {
    return false;
  }
}

function findPython() {
  const candidates = [
    process.env.WANJUAN_BUNDLED_DEFACE_PYTHON,
    isWindows ? "python.exe" : "python3.12",
    isWindows ? "py.exe" : "python3.11",
    isWindows ? "python3.exe" : "python3.10",
    "python3",
    "python"
  ].filter(Boolean);

  for (const candidate of candidates) {
    if (candidate === "py.exe" && commandWorks(candidate, ["-3.12", "--version"])) return { command: candidate, prefixArgs: ["-3.12"] };
    if (commandWorks(candidate, ["--version"])) return { command: candidate, prefixArgs: [] };
  }
  throw new Error("No usable Python found. Install Python 3.10+ or set WANJUAN_BUNDLED_DEFACE_PYTHON.");
}

function writeLicenseNote() {
  fs.writeFileSync(
    licensePath,
    [
      "# Bundled Deface Notices",
      "",
      "This folder is generated by `npm run prepare:bundled-deface`.",
      "",
      "- Deface: https://github.com/ORB-HD/deface",
      "- License: MIT",
      `- Bundled version: ${defaceVersion}`,
      "- Runtime dependencies are installed into the local virtual environment during release preparation.",
      "",
      "Do not place Qwen-TTS here. Qwen-TTS is distributed as an official optional offline tool pack."
    ].join("\n"),
    "utf8"
  );
}

function writeManifest() {
  fs.writeFileSync(
    manifestPath,
    JSON.stringify({
      name: "Deface",
      id: "deface",
      version: defaceVersion,
      platform: platformKey,
      arch: archKey,
      preparedAt: new Date().toISOString(),
      command: path.relative(platformRoot, bundledDefaceCommandPath())
    }, null, 2),
    "utf8"
  );
}

function bundledDefaceCommandPath() {
  if (isWindows) return venvDeface;
  return path.join(platformRoot, "bin", "deface");
}

function writeNonWindowsWrapper() {
  if (isWindows) return;
  const binRoot = path.join(platformRoot, "bin");
  const wrapperPath = path.join(binRoot, "deface");
  fs.mkdirSync(binRoot, { recursive: true });
  fs.writeFileSync(
    wrapperPath,
    [
      "#!/bin/sh",
      "set -eu",
      "ROOT=\"$(CDPATH= cd -- \"$(dirname -- \"$0\")/..\" && pwd)\"",
      "exec \"$ROOT/deface/venv/bin/python\" \"$ROOT/deface/venv/bin/deface\" \"$@\""
    ].join("\n"),
    "utf8"
  );
  fs.chmodSync(wrapperPath, 0o755);
}

function ensureVirtualenv(python) {
  if (commandWorks(python.command, [...python.prefixArgs, "-m", "virtualenv", "--version"])) {
    return { command: python.command, prefixArgs: python.prefixArgs };
  }
  if (!fs.existsSync(bootstrapPython)) {
    fs.rmSync(bootstrapRoot, { recursive: true, force: true });
    run(python.command, [...python.prefixArgs, "-m", "venv", bootstrapRoot]);
  }
  run(bootstrapPython, ["-m", "pip", "install", "--upgrade", "pip", "setuptools", "wheel", "virtualenv"]);
  return { command: bootstrapPython, prefixArgs: [] };
}

function createPortableVenv(python) {
  fs.rmSync(venvRoot, { recursive: true, force: true });
  if (tryRun(python.command, [...python.prefixArgs, "-m", "venv", "--copies", venvRoot])) return;
  fs.rmSync(venvRoot, { recursive: true, force: true });
  const virtualenvPython = ensureVirtualenv(python);
  run(virtualenvPython.command, [...virtualenvPython.prefixArgs, "-m", "virtualenv", "--copies", venvRoot]);
}

function assertNoAbsolutePythonLink() {
  if (isWindows) return;
  const pythonVersionPath = path.join(venvBin, "python3.12");
  try {
    const linkTarget = fs.readlinkSync(pythonVersionPath);
    if (path.isAbsolute(linkTarget)) {
      throw new Error(`Bundled venv Python is an absolute symlink: ${pythonVersionPath} -> ${linkTarget}`);
    }
  } catch (error) {
    if (error?.code === "EINVAL") return;
    throw error;
  }
}

if (commandWorks(bundledDefaceCommandPath(), ["--version"]) || commandWorks(bundledDefaceCommandPath(), ["--help"])) {
  assertNoAbsolutePythonLink();
  writeLicenseNote();
  writeManifest();
  console.log(`Bundled Deface already prepared: ${bundledDefaceCommandPath()}`);
  process.exit(0);
}

const python = findPython();
fs.mkdirSync(targetRoot, { recursive: true });
if (!fs.existsSync(venvPython)) {
  createPortableVenv(python);
}

run(venvPython, ["-m", "pip", "install", "--upgrade", "pip", "setuptools", "wheel"]);
run(venvPython, ["-m", "pip", "install", "--upgrade", `deface==${defaceVersion}`]);
writeNonWindowsWrapper();
assertNoAbsolutePythonLink();

if (!commandWorks(bundledDefaceCommandPath(), ["--version"]) && !commandWorks(bundledDefaceCommandPath(), ["--help"])) {
  throw new Error(`Deface was installed but is not runnable: ${bundledDefaceCommandPath()}`);
}

writeLicenseNote();
writeManifest();
console.log(`Bundled Deface prepared: ${bundledDefaceCommandPath()}`);
